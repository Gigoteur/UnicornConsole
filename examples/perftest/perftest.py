def perftest(name, f, iterations):
    start = time.time()
    for i in range(iterations):
        f()
    end = time.time()
    elapsed_time = end - start
    time_per_op = elapsed_time * 1000000 / iterations
    print("%-20s%8d%20.3f%20.3f" %
          (name, iterations, elapsed_time, time_per_op))


